function npcGen(building, first) {
  let firstNPC = first;
  if (firstNPC === undefined) {
    firstNPC = 0;
  }
  const id = npcs.length;// id
  // occupation generation
  let child = 0;
  let randInt = 0;// setup variable to hold randomly generated number
  let job = 'tempJob';// temporary job name
  for (let i = 0; i < buildingList.length; i++) {
    if (buildingList[i].building === building) {
      if (firstNPC === 1) {
        job = buildingList[0].jobs[0];
        break;
      } else {
        randInt = Math.floor(Math.random() * buildingList[i].jobs.length);
        job = buildingList[i].jobs[randInt];
        if (job === 'Youth') {
          child = 1;// set child flag as true
        }
        break;
      }
    }
  }
  // age range generation
  randInt = Math.floor(Math.random() * age.length);
  let ageNPC = age[randInt];
  if (child === 1) {
    ageNPC = 'Child';
  }
  // demeanor gen
  randInt = Math.floor(Math.random() * demean.length);
  const demeanor = demean[randInt];
  // race gen
  let raceNPC = 'temp';
  const raceWeightSum = (humanChance + elfChance + dwarfChance + orcChance);
  randInt = Math.floor(Math.random() * raceWeightSum);// generate number within total weight
  if (raceWeightSum < humanChance) { // is number between 0 and (humanChance)
    raceNPC = raceName[0];
  } else if (raceWeightSum < (humanChance + elfChance)) { // is number between (humanChance) & (elfChance)
    raceNPC = raceName[1];
  } else if (raceWeightSum < (humanChance + elfChance + dwarfChance)) { // is number between (elfChance) & (dwarfChance)
    raceNPC = raceName[2];
  } else { // third if statement failed, number must be between (dwarfChance) and sum
    raceNPC = raceName[3];
  }
  // gender and name gen
  randInt = Math.floor(Math.random() * 2);
  let gender = 'tempGen';// temp gender
  let name = 'tempName';// temp name
  for (let i = 0; i < race.length; i++) {
    if (race[i].race === raceNPC) {
      if (randInt === 0) {
        gender = 'Male';
        randInt = Math.floor(Math.random() * race[i].male.length);// first name gen
        name = race[i].male[randInt];
        randInt = Math.floor(Math.random() * race[i].surname.length);// surname gen
        name += race[i].surname[randInt];
        break;
      } else {
        gender = 'Female';
        randInt = Math.floor(Math.random() * race[i].female.length);
        name = race[i].female[randInt];
        randInt = Math.floor(Math.random() * race[i].surname.length);// surname gen
        name += race[i].surname[randInt];
        break;
      }
    }
  }
  // relationship gen
  let relation = 'None';
  if (noRel.length !== 0) { // check if there would be anyone to pair with
    randInt = Math.floor(Math.random() * relationChance);
    if (randInt === 0) {
      randInt = Math.floor(Math.random() * relationName.length);
      if (child === 1) { // so children won't generate a "significant other"
        randInt = Math.floor(Math.random() * relationName.length - 1);
      }
      relation = relationName[randInt];
      randInt = Math.floor(Math.random() * noRel.length);
      const relID = noRel[randInt];
      noRel.splice(randInt, 1);// remove NPC from noRel list
      for (let i = 0; i < npcs.length; i++) {
        if (relID === npcs[i].id) {
          npcs[i].relationship = relation + name;// put together relation and name for existing NPC
          relation += npcs[i].name;// put together relation and name for new NPC
          break;// stop searching
        }
      }
    } else { // add id to no relationship pool
      noRel.push(id);
    }
  } else { // no one in the list, push current npc in
    noRel.push(id);
  }
  const newNPC = new NPC(id, job, ageNPC, demeanor, raceNPC, gender, name, relation);
  npcs.push(newNPC);
}
